jQuery(document).ready(function($) {


    $(".content_faq").slideUp(); // element có class="content_faq" sẽ bị ẩn đi (hiệu ứng slideUp)
    $(".ldp-section5 .line_faq_group").on("click", function() {
        // khi click vào element có class="line_faq_group" thì sẽ thực hiện các hành động bên dưới

        if ($(this).hasClass("active")) {
            // nếu element có class="line_faq_group" đang có class="active"
            console.log("1");
            console.log(this);

            $(this).children(".content_faq").slideUp();
            // thì element có class="content_faq" sẽ bị ẩn đi (hiệu ứng slideToggle) ----- parent(): lấy phần tử cha của element hiện tại ----- find(): tìm kiếm các phần tử con của element hiện tại
            $(this).removeClass("active");
            // và element có class="line_faq_group" sẽ bị xóa class="active"
        } else { // ngược lại
            console.log("2");
            $('.ldp-section5 .line_faq_group').removeClass("active");
            // khi element có class="line_faq_group" bị xóa class="active"
            $(this).addClass("active");
            // thì element hiện tại sẽ được thêm class="active"
            $(".line_faq_group .content_faq").slideUp();
            // element có class="content_faq" sẽ bị ẩn đi (hiệu ứng slideUp)
            $(this).children(".content_faq").slideDown();
            // element có class="content_faq" sẽ hiện ra (hiệu ứng slideDown)
        }

    });
});
</script>